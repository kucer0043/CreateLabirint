def resource_path(relative_path):
    try:
        # PyInstaller creates a temp folder and stores path in _MEIPASS
        base_path = sys._MEIPASS
    except Exception:
        base_path = os.path.abspath(".")

    return os.path.join(base_path, relative_path)


def H_W_rect(size_rect):  # задаёт размеры квадратов, в пропоции к карте и количеству кубиков
    x, y = size_rect
    x = int(x / rects_on_screen)
    y = int(y / rects_on_screen)
    return x, y


def get_lan_ip():
    for iface in netifaces.interfaces():
        if iface == 'lo' or iface.startswith('vbox'):
            continue
        iface_details = netifaces.ifaddresses(iface)
        if netifaces.AF_INET in iface_details:
            yield iface_details[netifaces.AF_INET][0]["addr"]


